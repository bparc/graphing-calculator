static inline void SetVector(GLfloat* V, float x, float y)
{
	V[0] = x;
	V[1] = y;
}

static inline void SetUV(GLfloat* V, float x, float y)
{
	V[2] = x;
	V[3] = y;
}

static inline void SetColor(GLfloat* V, float x, float y, float z, float w)
{
	V[4] = x;
	V[5] = y;
	V[6] = z;
	V[7] = w;
}

static void PushTexturedRectangle(renderer_t* state, float x, float y, float width, float height, float r, float g, float b, float a, float uv_min_x, float uv_min_y, float uv_max_x, float uv_max_y)
{
	int32_t count = (4 * STRIDE);
	assert(state->Offset + count <= ArraySize(state->Vertices));
	if (state->Offset + count <= ArraySize(state->Vertices))
	{
		float min_x = x;
		float min_y = y;
		float max_x = (min_x + width);
		float max_y = (min_y + height);
		GLfloat* result = (state->Vertices + state->Offset);
		GLfloat* V1 = result + (0 * STRIDE);
		GLfloat* V2 = result + (1 * STRIDE);
		GLfloat* V3 = result + (2 * STRIDE);
		GLfloat* V4 = result + (3 * STRIDE);
		SetVector(V1, min_x, min_y);
		SetVector(V2, max_x, min_y);
		SetVector(V3, max_x, max_y);
		SetVector(V4, min_x, max_y);
		SetColor(V1, r, g, b, a);
		SetColor(V2, r, g, b, a);
		SetColor(V3, r, g, b, a);
		SetColor(V4, r, g, b, a);
		SetUV(V1, uv_min_x, uv_min_y);
		SetUV(V2, uv_max_x, uv_min_y);
		SetUV(V3, uv_max_x, uv_max_y);
		SetUV(V4, uv_min_x, uv_max_y);
		state->Offset += count;
	}
}

static render_command_t *PushRenderCommand(renderer_t* State, GLuint Program)
{
	if(State->CommandCount > 0)
	{
		State->Commands[State->CommandCount - 1].Count = ((State->Offset / STRIDE) - (State->Commands[State->CommandCount - 1].Offset/STRIDE));
	}
	assert(State->CommandCount < ArraySize(State->Commands));
	render_command_t* result = State->Commands + State->CommandCount++;
	memset(result, 0, sizeof(*result));
	result->Program = Program;
	result->Offset = (State->Offset)/STRIDE;
	return result;
}

static GLuint CompileShader(GLenum type, const char* source)
{
	GLuint result = glCreateShader(type);
	glShaderSource(result, 1, &source, 0);
	glCompileShader(result);
#if _DEBUG
	GLint status = 0;
	glGetShaderiv(result, GL_COMPILE_STATUS, &status);
	if ((status == GL_FALSE))
	{
		char infoLog[256] = "";
		glGetShaderInfoLog(result, sizeof(infoLog), 0, infoLog);
		printf("%s\n", infoLog);
	}
#endif
	return result;
}

extern GLuint CompileProgram(const char* vertexSource, const char *fragmentSource)
{
	GLuint shaders[2] =
	{
		CompileShader(GL_VERTEX_SHADER,vertexSource),
		CompileShader(GL_FRAGMENT_SHADER,fragmentSource),
	};
	GLuint result = glCreateProgram();
	glAttachShader(result, shaders[0]);
	glAttachShader(result, shaders[1]);
	glLinkProgram(result);
	glDeleteShader(shaders[0]);
	glDeleteShader(shaders[1]);
#if _DEBUG
	GLint status = 0;
	glGetProgramiv(result, GL_LINK_STATUS, &status);
	if ((status == GL_FALSE))
	{
		char infoLog[256] = "";
		glGetProgramInfoLog(result, sizeof(infoLog), 0, infoLog);
		printf("%s\n", infoLog);
		glDeleteProgram(result);
		result = 0;
	}
#endif
	return result;
}

extern void BeginViewport(renderer_t* state,int32_t viewport[4], int32_t flags)
{
	if ((state->MemoryInitialized == 0))
	{
		glGenBuffers(1, &state->VBO);
		glGenVertexArrays(1, &state->VAO);

		glBindVertexArray(state->VAO);
		glBindBuffer(GL_ARRAY_BUFFER, state->VBO);
		glClientActiveTexture(GL_TEXTURE0);
		glEnableVertexAttribArray(0);
		glEnableVertexAttribArray(1);
		glEnableVertexAttribArray(3);
		glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, (sizeof(GLfloat) * STRIDE), 0);
		glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, (sizeof(GLfloat) * STRIDE), (const void*)(sizeof(GLfloat) * 2));
		glVertexAttribPointer(3, 4, GL_FLOAT, GL_FALSE, (sizeof(GLfloat) * STRIDE), (const void*)(sizeof(GLfloat) * 4));
		state->Program = CompileProgram(
			"\
			#version 330 core\n\
			uniform mat4 Transform;\
			layout (location = 0) in vec2 VertexPosition;\
			layout (location = 1) in vec2 VertexUV;\
			layout (location = 3) in vec4 VertexColor;\
			out vec4 Color;\
			out vec2 UV;\
			void main()\
			{\
			Color = VertexColor;\
			UV = VertexUV;\
			gl_Position = vec4(VertexPosition.x,VertexPosition.y,\
			0.0,1.0) * Transform;\
			}",
			"\
			#version 330 core\n\
			out vec4 PixelColor;\
			in vec2 UV;\
			in vec4 Color;\
			uniform sampler2D Texture;\
			void main()\
			{\
			PixelColor = texture(Texture, UV) * Color;\
			}"
			);

		// NOTE(): Font
		glGenTextures(1, &state->TextureHandle);
#define X 128
#define Y 48
		static uint32_t RGBAGlyphs[Y][X];
		for (int32_t y = 0; y < Y; y++)
		{
			for (int32_t x = 0; x < X; x++)
			{
				RGBAGlyphs[y][x] = _Glyphs[y][x] ?
					0xffffffff : 0;
			}
		}	
		glBindTexture(GL_TEXTURE_2D, state->TextureHandle);
		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, X, Y, 0, GL_RGBA, GL_UNSIGNED_BYTE, RGBAGlyphs);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
		glGenerateMipmap(GL_TEXTURE_2D);
		state->TextureScale[0] = 1.0f / (float)X;
		state->TextureScale[1] = 1.0f / (float)Y;
#undef X
#undef Y

		assert(state->Program && state->TextureHandle);
		state->MemoryInitialized = TRUE;
	}

	state->UniformCount = 0;
	state->Offset = 0;
	state->Viewport[0] = viewport[0];
	state->Viewport[1] = viewport[1];
	state->Viewport[2] = viewport[2];
	state->Viewport[3] = viewport[3];

	state->CommandCount = 0;
	PushRenderCommand(state, state->Program);

	assert(!state->Rendering);
	state->Rendering = TRUE;
}

extern void EndViewport(renderer_t* state)
{
	PushRenderCommand(state, state->Program);
	assert(state->Rendering);
	state->Rendering = 0;
}

extern void BeginProgram(renderer_t* State, GLuint Program, uniform_t* Uniforms, int32_t UniformCount)
{
	assert(State->CustomProgram == 0);
	State->CustomProgram = Program;
	render_command_t *result = PushRenderCommand(State, Program);
	if (Uniforms)
	{
		assert(State->UniformCount + UniformCount <= ArraySize(State->UniformBuffer));
		memcpy(State->UniformBuffer, Uniforms, sizeof(uniform_t) * UniformCount);
		result->UniformCount = UniformCount;
		result->Uniforms = State->UniformBuffer;
		State->UniformCount += UniformCount;
	}
}

extern void EndProgram(renderer_t* State)
{
	assert(State->CustomProgram != 0);
	State->CustomProgram = 0;
	PushRenderCommand(State, State->Program);
}

extern void OutputRenderCommands(renderer_t* state)
{
	glViewport(state->Viewport[0], state->Viewport[1],
		state->Viewport[2], state->Viewport[3]);
	assert((state->Rendering == 0));
	float A = (1.0f / ((float)state->Viewport[2]*0.5f));
	float B = -(1.0f / ((float)state->Viewport[3]*0.5f));
	float C = -1;
	float D = +1;
	GLfloat M[4][4] =
	{
		A,0,0,C,
		0,B,0,D,
		0,0,1,0,
		0,0,0,1,
	};
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, state->TextureHandle);
	glEnable(GL_BLEND);
	glEnable(GL_TEXTURE_2D);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);
	glBindBuffer(GL_ARRAY_BUFFER, state->VBO);
	glBindVertexArray(state->VAO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(GLfloat) * state->Offset, state->Vertices, GL_STATIC_DRAW);
 	for (int32_t index = 0; index < state->CommandCount; index++)
	{
		render_command_t* command = (state->Commands + index);
		glUseProgram(command->Program);
		glUniform1i(glGetUniformLocation(command->Program, "Texture"), 0);
		glUniformMatrix4fv(glGetUniformLocation(command->Program, "Transform"), 1,
			GL_FALSE, &M[0][0]);
		for (int32_t index = 0; index < command->UniformCount; index++)
		{
			uniform_t* uniform = command->Uniforms + index;
			switch (uniform->Type)
			{
			case UNIFORM_TYPE_V2:
			{
				glUniform2f(glGetUniformLocation(command->Program, uniform->Name), uniform->V2.x, uniform->V2.y);
			} break;
			default:
			{
				assert(0);
			}
			}
		}
#if 1
		glDrawArrays(GL_QUADS, command->Offset, command->Count);
#endif
	}
}

extern void RenderString(renderer_t* state, const char* string, float x, float y)
{
	x = floorf(x);
	y = floorf(y);
	float pixelHeight = 8.0f;
	while (*string++)
	{
		int32_t codepoint = toupper((int32_t) * (string - 1));
		if (codepoint)
		{
			float uv_min_x = (float)((codepoint % 16) * 8);
			float uv_min_y = (float)((codepoint / 16) * 8);
			float uv_max_x = (uv_min_x + 8.0f);
			float uv_max_y = (uv_min_y + 8.0f);
			uv_min_x *= state->TextureScale[0];
			uv_min_y *= state->TextureScale[1];
			uv_max_x *= state->TextureScale[0];
			uv_max_y *= state->TextureScale[1];
			PushTexturedRectangle(state, x, y, pixelHeight, pixelHeight, 1.0f, 1.0f, 1.0f,1.0f, uv_min_x, uv_min_y, uv_max_x, uv_max_y);
			x += pixelHeight;
		}
	}
}

extern void RenderStringCentered(renderer_t* state, const char* string, rectangle_t Bounds)
{
	int32_t count = CountString(string);
	Bounds.X += ((Bounds.W * 0.5f) - ((float)(count * 8) * 0.5f));
	Bounds.Y += (Bounds.H * 0.5f - 4.0f);
	RenderString(state, string, Bounds.X, Bounds.Y);
}

